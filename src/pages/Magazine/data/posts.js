export function getPostBySlug(slug) {
  return POSTS.find(p => p.slug === slug) || null;
}

export function getPostsByCategory(categoryId) {
  return POSTS.filter(p => p.category === categoryId);
}

export function getFeaturedPosts(count = 4) {
  return POSTS.filter(p => p.featured).slice(0, count);
}

export function getTrendingPosts(count = 5) {
  return POSTS.filter(p => p.trending).slice(0, count);
}

export function getLatestPosts(count = 8, excludeId = null) {
  return POSTS
    .filter(p => p.id !== excludeId)
    .slice(0, count);
}

export function getRelatedPosts(post, count = 3) {
  return POSTS
    .filter(p => p.id !== post.id && p.category === post.category)
    .slice(0, count);
}

export default function Icon({
  name,
  size = 20,
  color = "currentColor",
  strokeWidth = 1.5,
  fill = "none",
  style,
  className,
  ...rest
}) {
  const d = paths[name];
  if (!d) return null;

  // Some icons need fill instead of stroke
  const fillIcons = ["heartFilled", "starFilled", "sparkleMini"];
  const useFill = fillIcons.includes(name);

  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={useFill ? color : fill}
      stroke={useFill ? "none" : color}
      strokeWidth={useFill ? 0 : strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      style={{ display: "inline-block", verticalAlign: "middle", flexShrink: 0, ...style }}
      className={className}
      aria-hidden="true"
      {...rest}
    >
      {d}
    </svg>
  );
}

// Convenience: star rating row
export function StarRating({ rating, size = 10, color = "#C9A84C" }) {
  return (
    <span style={{ display: "inline-flex", gap: 1 }}>
      {[1, 2, 3, 4, 5].map((i) => (
        <Icon key={i} name={i <= Math.round(rating) ? "starFilled" : "star"} size={size} color={color} strokeWidth={1} />
      ))}
    </span>
  );
}
